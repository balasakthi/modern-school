package com.balsa.school.bean;

public class AdmissionStrength {

	Integer totalStudents, totalAdmission, totalMatric, totalNewPrePrimary, totalNewPrimary, totalNewSecondary,
			totalNewHigherSecondary, totalOldHigherSecondary;

	public AdmissionStrength() {
	}

	public Integer getTotalAdmission() {
		return totalAdmission;
	}

	public void setTotalAdmission(Integer totalAdmission) {
		this.totalAdmission = totalAdmission;
	}

	public Integer getTotalMatric() {
		return totalMatric;
	}

	public void setTotalMatric(Integer totalMatric) {
		this.totalMatric = totalMatric;
	}

	public Integer getTotalNewPrePrimary() {
		return totalNewPrePrimary;
	}

	public void setTotalNewPrePrimary(Integer totalNewPrePrimary) {
		this.totalNewPrePrimary = totalNewPrePrimary;
	}

	public Integer getTotalNewPrimary() {
		return totalNewPrimary;
	}

	public void setTotalNewPrimary(Integer totalNewPrimary) {
		this.totalNewPrimary = totalNewPrimary;
	}

	public Integer getTotalNewSecondary() {
		return totalNewSecondary;
	}

	public void setTotalNewSecondary(Integer totalNewSecondary) {
		this.totalNewSecondary = totalNewSecondary;
	}

	public Integer getTotalNewHigherSecondary() {
		return totalNewHigherSecondary;
	}

	public void setTotalNewHigherSecondary(Integer totalNewHigherSecondary) {
		this.totalNewHigherSecondary = totalNewHigherSecondary;
	}

	public Integer getTotalOldHigherSecondary() {
		return totalOldHigherSecondary;
	}

	public void setTotalOldHigherSecondary(Integer totalOldHigherSecondary) {
		this.totalOldHigherSecondary = totalOldHigherSecondary;
	}

	public Integer getTotalStudents() {
		return totalStudents;
	}

	public void setTotalStudents(Integer totalStudents) {
		this.totalStudents = totalStudents;
	}

}
